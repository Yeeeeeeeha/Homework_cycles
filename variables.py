# Описание компьютерного процессора

# Марка производителя
mark = 'Intel'
print(mark)

# Модель
model = 'i5 8400K'
print(model)

# Год производства
year_of_issue = 2017
print(year_of_issue)

# Количество ядер
number_of_cores = 4
print(number_of_cores)

# Тактовая частота процессора
сlock_frequency = 3.2
print(сlock_frequency)

# Возможность усовершенствования(разгона)
overclocking = True
print(overclocking)

# Типы вышеперечисленных переменных
print(type(mark))
print(type(model))
print(type(year_of_issue))
print(type(number_of_cores))
print(type(сlock_frequency))
print(type(overclocking))
